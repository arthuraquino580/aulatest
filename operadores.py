'''
Operadores lógicos
== para realizar comparação
!= Verifica se é diferente
> maior que
< menor que
>= maior ou igual 
<= menor ou igual
=+ incremento
-= decremento
'''

valor = 3
valor +=5

print(valor)

if valor > 3:
    print(f"{valor} é maior que 3")
elif valor <= 3:
    print(f"{valor} é maior que 3")
else:
    print("Os valores são iguais")